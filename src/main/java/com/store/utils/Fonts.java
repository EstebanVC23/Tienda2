package com.store.utils;

import java.awt.Font;

/**
 * Clase utilitaria que define las fuentes tipográficas utilizadas en la aplicación.
 * Contiene constantes para diferentes estilos y tamaños de fuentes.
 */
public class Fonts {
    /** Fuente para títulos principales (24px, negrita) */
    public static final Font TITLE = new Font("Segoe UI", Font.BOLD, 24);
    
    /** Fuente para subtítulos (18px, negrita) */
    public static final Font SUBTITLE = new Font("Segoe UI", Font.BOLD, 18);
    
    /** Fuente para títulos de sección (14px, negrita) */
    public static final Font SECTION_TITLE = new Font("Segoe UI", Font.BOLD, 14);
    
    /** Fuente para texto en negrita (12px, negrita) */
    public static final Font BOLD_BODY = new Font("Segoe UI", Font.BOLD, 12);
    
    /** Fuente para texto normal del cuerpo (12px, normal) */
    public static final Font BODY = new Font("Segoe UI", Font.PLAIN, 12);
    
    /** Fuente para botones (14px, negrita) */
    public static final Font BUTTON = new Font("Segoe UI", Font.BOLD, 14);
    
    /** Fuente para texto pequeño (11px, normal) */
    public static final Font SMALL = new Font("Segoe UI", Font.PLAIN, 11);
}